    // MAIN AUTO-RESIZE METHOD
    private void printBox(List<String> lines) {
        int longest = 0;

        // find longest line
        for (String s : lines) {
            if (s.length() > longest) longest = s.length();
        }

        int padding = 5; // extra spaces left + right
        int totalWidth = longest + padding * 2;

        String top = "╔" + "═".repeat(totalWidth) + "╗";
        String bottom = "╚" + "═".repeat(totalWidth) + "╝";

        System.out.println(top);

        // print each line padded properly
        for (String s : lines) {
            int spaces = totalWidth - s.length();
            int left = padding;
            int right = spaces - left;
            System.out.println("║" + " ".repeat(left) + s + " ".repeat(right) + "║");
        }

        System.out.println(bottom);
    }
